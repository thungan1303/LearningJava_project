package day03_variables;

public class PrimitiveTypeChar {

	public static void main(String[] args) {
		
		char letter = 'a';
		char digit = '9';
		char letter2 = '^';
		char letter3 = ' ';
		
		System.out.println(letter); // a
		System.out.println(letter2); // ^
		System.out.println(letter3); // 
		System.out.println(digit); // 9

	}

}
